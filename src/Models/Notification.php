<?php

namespace AbdullahMateen\LaravelHelpingMaterial\Models;

use AbdullahMateen\LaravelHelpingMaterial\Enums\Notification\StatusEnum;
use Illuminate\Database\Eloquent\Model;

class Notification extends Model
{
    /*
    |--------------------------------------------------------------------------
    | Properties
    |--------------------------------------------------------------------------
    */

    protected $casts = [
        'data'      => 'array',
        'status'    => StatusEnum::class,
        'send_at'   => 'datetime',
        'exception' => 'array',
    ];

    protected $guarded = [];

    /*
    |--------------------------------------------------------------------------
    | Override Methods
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | Contracts / Traits
    |--------------------------------------------------------------------------
    */


    /*
    |--------------------------------------------------------------------------
    | Const Variables
    |--------------------------------------------------------------------------
    */


    /*
    |--------------------------------------------------------------------------
    | Scope Methods
    |--------------------------------------------------------------------------
    */

    public function scopeRead($query)
    {
        return $query->where('status', '=', StatusEnum::Read->value);
    }

    public function scopeUnread($query)
    {
        return $query->where('status', '=', StatusEnum::Unread->value);
    }

    /*
    |--------------------------------------------------------------------------
    | Authorization
    |--------------------------------------------------------------------------
    */


    /*
    |--------------------------------------------------------------------------
    | Validations
    |--------------------------------------------------------------------------
    */


    /*
    |--------------------------------------------------------------------------
    | Mutators
    |--------------------------------------------------------------------------
    */


    /*
    |--------------------------------------------------------------------------
    | Accessors
    |--------------------------------------------------------------------------
    */


    /*
    |--------------------------------------------------------------------------
    | Helper Functions
    |--------------------------------------------------------------------------
    */


    /*
    |--------------------------------------------------------------------------
    | Relationships
    |--------------------------------------------------------------------------
    */

    public function sender()
    {
        return $this->belongsTo(_get_user_model()::class, 'sender_id');
    }

    public function receiver()
    {
        return $this->belongsTo(_get_user_model()::class, 'receiver_id');
    }

}
